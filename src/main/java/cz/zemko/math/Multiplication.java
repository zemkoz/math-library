package cz.zemko.math;

/**
 * Provides multiplication operation.
 *
 * @author Zoltan Zemko
 */
public final class Multiplication {

    /**
     * Compute multiplication of two values.
     *
     * @param a value
     * @param b value
     * @return a multiply b.
     */
    public static int compute(int a, int b) {
        return a * b;
    }
}
