package cz.zemko.math;

/**
 * Provides subtraction operation.
 *
 * @author Zoltan Zemko
 */
public final class Subtraction {

    /**
     * Compute subtraction of two values.
     *
     * @param a value
     * @param b value
     * @return a minus b.
     */
    public static int compute(int a, int b) {
        return a - b;
    }
}
